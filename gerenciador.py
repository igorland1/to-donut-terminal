def remover_tarefas_concluidas(tarefas):
    count = sum(1 for t in tarefas if t["concluida"])
    for tarefa in tarefas[:]:
        if tarefa["concluida"]:
            tarefas.remove(tarefa)
    if count > 0:
        print(f"{CARAMELO}üç© Limpando as migalhas... {count} donut(s) removido(s)!{Style.RESET_ALL}")
        salvar_tarefas(tarefas)
    else:
        print(f"{CHOCOLATE}üç© Nenhuma migalha pra limpar! Caixa j√° est√° limpa!{Style.RESET_ALL}")

